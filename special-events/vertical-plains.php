              <h2>2018 Vertical Plains Adventure Festival</h2>
              
              

              <h3>What is the Vertical Plains Adventure Festival?</h3>
              <p>Vertical Plains is a celebration of the world we live in with a little climbing thrown in. The Vertical Plains Climbing Competition, West Texas' best competition for skilled and beginning rock climbers, will take place on Saturday. </p>
              <p>The 2018 Vertical Plains Adventure Festival will be held on Saturday, February 3rd, 2018. Over the course of the years, we have had teams from across the region compete, and claim prizes. Returning and first-time participants will be sure to find that this is the best Vertical Plains yet! See you on the wall! 
              </p>
              <h3>Vertical Plains Climbing Competition</h3>
<p>The annual Vertical Plains Climbing Competition is the biggest event hosted   by the Outdoor Pursuits Center every year, and this year's 18th annual event will be no different. It is a climbing competition that   draws over 100 competitors from around the southwest and promotes camaraderie,   sportsmanship, and the love of climbing. We encourage everyone, regardless of their experience, to join in the   competition and have a great time!</p>
