                           <h3>Personal Standard Fitness Assessment/Advanced Sub-Max Fitness Analyses <a id="3"></a></h3>
                           <p><strong>Standard Fitness Assessment</strong></p>
                           <p>Before you start any exercise program, it is important to know your fitness level.
                              Our Personal Fitness Assessment is designed to give you a foundation to track your
                              progress throughout your exercise program. Subsequent assessments will provide milestones
                              to help you evaluate your progress and set goals. Our assessment is normally administered
                              during your initial consultation with your personal trainer, consisting of the following
                              health measurements: height, weight, blood pressure, heart rate, body composition,
                              cardiovascular fitness, muscular strength, endurance, and flexibility. This is included
                              in your training package price.
                           </p>
                           <p><strong>Advanced Sub-Max Fitness Analyses <br> $45 w/ purchase Training Package or $50 set price</strong></p>
                           <p>Let exercise physiology help you improve your workouts. This test includes the Standard
                              Fitness Assessment plus a submaximal bicycle ergometer test and a sit down with the
                              personal trainer to explain what the results mean. ACSM guidelines will be followed
                              and doctor's approval may be required.
                           </p>
                          

