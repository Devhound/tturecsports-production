        <h2 id="6">New Client Information</h2>
        
        <p>On your first appointment, there will be a general health form to complete, which will help inform us about your current state of health. An initial consultation between you and your therapist will then take place to discuss your session and any concerns that you might have.</p>

        <p>Most massage sessions generally last an hour. We suggest that you allow 1 hour and 15 min. for each appointment.  This allows you 10 min. before the massage to check in, fill out any needed health forms, and to meet with your massage therapist.  It also provides you 5 minutes, after the massage, to get dressed.</p>
        <p> Making you feel extremely comfortable is one of our main goals.  You can remove as much or as little clothing as you are comfortable with.  With this explained, the therapist will leave the room while you undress to your comfort level.  Since many massage techniques involve lubricating the skin with oils and lotions, it is common to be completely undressed.  This isn’t a requirement by any means and the therapist will respect your boundaries.  Your privacy will always be respected and you will be covered or draped with towels or sheets so that only the body part being worked on is exposed at any given time. To begin, you will lie face down underneath the top sheet. There is a buzzer on the small table that you can push to let the therapist know you are ready for your massage.          </p>
        <p>Massage should be a safe, non-invasive and personal experience where the client is in control.  Respect for the client’s boundaries is our first priority.  Each individual’s musculature, tolerance to pressure, and joint flexibility are different.  It is important for you to give feedback to the massage therapist so that each session can be tailored to your individual needs. Conversation with your massage therapist, during a session, depends on your need to talk.  Some clients desire silence.  We will try to accommodate what the client wishes.  Relaxing music will be provided. Please feel comfortable giving feedback to your therapist.</p>
        <p>You can workout before a massage but please allow 30 minutes for your body to relax and get back to normal blood pressure and pulse. Please <strong>SHOWER</strong> before your massage if you do a workout. Not only for sanitary reasons, but also our lotion will not allow a smooth glide with certain techniques. Depending on the type of massage you received and how long it was, you may or may not want to workout after a massage. The deeper the work and the longer your session, the more time your body needs for recovery. If you had a long, relaxing session, your body will not feel like doing anything but relaxing.</p>

        <p><strong>Massage oils or lotions will be used whenever there is skin contact</strong>.  If you have concerns about an allergic reaction please let us know. </p>


