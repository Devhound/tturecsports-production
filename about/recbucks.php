            <h4>What is Rec Sports Association?</h4>
            <p>Rec Sports Association (RSA) is a student organization open to all student employees of the TTU Department of Recreational Sports. This student organization hosts community service events, fundraisers, and socials! Please join our organization on OrgSync to get all the updates, see the calendar of events, and to have access to the RecBuck Program. </p>

            <h4>What are Rec Bucks?</h4>
            <p>RecBucks is a  student staff appreciation program that has partnered with RSA to promote both entities. It has been developed to recognize students that go above and beyond their daily job responsibilities. This program is only available for student staff members employed by the Department of Recreational Sports at Texas Tech. Rec Bucks can be spent on Rec Sports swag that you can use and wear inside and outside of the Rec. Without our students, we wouldn't be able to do all the amazing things we do here in support of the TTU community, so we want to do more to appreciate you all! </p>

