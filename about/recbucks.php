            <h4>How do we keep track of  RecBucks?</h4>
            <p>Each time a student does something or attends an event that is eligible to earn RecBucks, a student will need to submit a <a href="https://orgsync.com/89666/forms/273213">Earned RecBucks Form</a>. </p>

            <p>As you can tell, there are ways for other students and professionals to give RecBucks to other students via Staff Props. If this is the case, the person giving the staff props will need to submit a Earned RecBucks Form for the person receiving them. This is the same for Professional Staff members giving RecBucks for students that go above and beyond their job responsibilties. The Professional Staff member giving the RecBucks will need to complete this form. </p>
            <p>We will keep a running total for each student based on these submissions. If a Earned RecBucks form is not submitted, we will not count it with your total points. Each semester, there will be a &quot;RecBuck Deadline&quot; for submissions in that term that is the Sunday of the weekend of Graduation for that term. The deadline to submit points earned for Fall 2017 shall be December 19th. </p>

            <h4>How can I spend my RecBucks?</h4>
            <p>In order to spend your RecBucks,  students just need to earn them<a href="https://orgsync.com/89666/forms/248879"></a>. As you accrue the RecBucks, you will be provided with the merchandise that you have earned by a Rec Sports Professional when you earn enough to get an item. </p>
            <p>RecBucks roll over from semester to semester as long as the student is employed by Rec Sports. Points cannot be traded or transferred from one student to another. If a student graduates and  still has RecBucks on an account, they will no longer be valid.</p>
            <p>If you have any questions, please email tturecsports@ttu.edu</p>

