            <h2>2018 Faculty/Staff Fitness Challenge</h2>

            <h3>Purpose</h3>
            <p>The purpose of the Texas Tech Faculty & Staff Fitness Challenge is to support, motivate, inform, and encourage our campus community to be active, eat well, and be fit.</p>

