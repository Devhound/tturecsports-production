            <p><b>Step 3:</b> Follow the guided set-up process and join the fun!</p>
            <p>As you build your team, keep in mind that not all members of your team may be at the same level of fitness or have the same fitness goals.  If you cannot find a team that is appropriate for your level of fitness, contact <a href="mailto:kami.waden@ttu.edu">Kami Waden</a> to assist you in finding a team.</p>

            <h3>Team Categories</h3>
            <p>Each team will need to choose with level to participate in. The entire team will be challenged to complete the minimum number of minutes for each level.</p>
            <p><b>Raider Rookie:</b> Complete 150 minutes of activity each week per person.</p>
            <p><b>Raider Power:</b> Complete 270 minutes of activity each week per person.</p>
            <p><b>Raider Warrior:</b> Complete 360 minutes of activity each week per person.</p>

            <p>The team captain will choose the level to compete at when they create the team online at <a href="http://ttu.fitthumb.com">ttu.fitthumb.com</a></p>

