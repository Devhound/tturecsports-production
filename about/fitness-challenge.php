            <p>Registration Starts: January 15</p>
            <p>Challenge Starts: January 22 </p>

            <h3>Create a team</h3>
            <p>Official team members must be full time, part time and graduate student TTU employees who are benefits eligible. Your teammates do not have to be in your department. Sorry, undergraduate student assistants are not eligible to participate. Get your team of 2 - 6 TTU employees together. Choose a captain. Team will register in 1 of 3 participation levels depending on their fitness goals:</p>
            <p><b>PLEASE NOTE - WE HAVE A NEW ONLINE SYSTEM, SO YOU WILL NEED TO CREATE A NEW USER ACCOUNT!</b></p>
            <p>To create your account and join the fun follow the simple steps below. </p>
