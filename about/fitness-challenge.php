            <h3>What Activities Can Be Counted?</h3>
            <p>The purpose of this program is to increase your daily activity level. This should be intentional and outside the scope of your normal activity. Walking to the restroom or to another office should not be counted; walking for 15 minutes of your lunchtime should be counted. Activity should be done at least at a moderate level. A simple way of determining this is the talk testâ€”you should be able to talk in short bouts but not able to carry on a casual conversation or sing. <a href="AFChallenge/How To Participate and Log Your Minutes.pdf" class="pdf">Review this PDF for more explanation. </a></p>


