            <h4> What can you do to prevent CA-MRSA?</h4>
            <p>Protecting yourself from CA-MRSA — which might be just about anywhere — may seem daunting, but these common-sense precautions can help reduce your risk:</p>
            <ul>
              <li> Keep personal items personal. Avoid sharing personal items such as towels, sheets, razors, clothing and athletic equipment. MRSA spreads on contaminated objects as well as through direct contact.            </li>
              <li>Keep wounds covered. Keep cuts and abrasions clean and covered with sterile, dry bandages until they heal. The pus from infected sores often contains MRSA, and keeping wounds covered will help keep the bacteria from spreading.            </li>
              <li>Sanitize linens. If you have a cut or sore, wash towels and bed linens in hot water with added bleach and dry them in a hot dryer. Wash gym and athletic clothes after each wearing.            </li>
              <li>Wash your hands. In or out of the hospital, careful hand washing remains your best defense against germs. Scrub hands briskly for at least 15 seconds, then dry them with a disposable towel and use another towel to turn off the faucet. Carry a small bottle of hand sanitizer containing at least 62 percent alcohol for times when you don't have access to soap and water.            </li>
              <li>Get tested. If you have a skin infection that requires treatment, ask your doctor if you should be tested for MRSA. Many doctors prescribe drugs that aren't effective against antibiotic-resistant staph, which delays treatment and creates more resistant germs.              </li>
              <li>Clean fitness equipment, mats, etc. before and after you use them.              </li>
            </ul>

