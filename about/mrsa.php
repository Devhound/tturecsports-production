            <h2>MRSA &amp; Swine Flu: The Facts</h2>
            <h4>What is MSRA?</h4>
            <p>Methicillin-resistant Staphylococcus aureus (MRSA) infection is caused by Staphylococcus aureus bacteria — often called "staph." Decades ago, a strain of staph emerged in hospitals that was resistant to the broad-spectrum antibiotics commonly used to treat it. Dubbed methicillin-resistant Staphylococcus aureus (MRSA), it was one of the first germs to outwit all but the most powerful drugs. MRSA infection can be fatal.</p>

            <p>Staph bacteria are normally found on the skin or in the nose of about one-third of the population. If you have staph on your skin or in your nose but aren't sick, you are said to be "colonized" but not infected with MRSA. Healthy people can be colonized with MRSA and have no ill effects, however, they can pass the germ to others.
            Staph bacteria are generally harmless unless they enter the body through a cut or other wound, and even then they often cause only minor skin problems in healthy people. But in older adults and people who are ill or have weakened immune systems, ordinary staph infections can cause serious illness called methicillin-resistant Staphylococcus aureus or MRSA.</p>

            <h4>What is CA-MSRA?</h4>
            <p>In the 1990s, a type of MRSA began showing up in the wider community. Today, that form of staph, known as community-associated MRSA, or CA-MRSA, is responsible for many serious skin and soft tissue infections and for a serious form of pneumonia.</p>

            <h4>What is H1N1 (Swine Flu)?</h4>
            <p>2009 H1N1 (sometimes called “swine flu”) is a new influenza virus causing   illness in people. This new virus was first detected in people in the United   States in April 2009. This virus is spreading from person-to-person worldwide,   probably in much the same way that regular seasonal influenza viruses spread. On   June 11, 2009, the <a href="http://www.who.int/csr/disease/swineflu/en/index.html" class="external" jquery1259880969543="133">World Health   Organization</a> (WHO) signaled that a   pandemic of 2009 H1N1 flu was underway.</p>

            <h4>How Does H1N1 Spread?</h4>
            <p>Spread of 2009 H1N1 virus is thought to occur in the same way that seasonal flu   spreads. Flu viruses are spread mainly from person to person through coughing or   sneezing by people with influenza. Sometimes people may become infected by   touching something – such as a surface or object – with flu viruses on it and   then touching their mouth or nose</p>

            <h4>What has the Rec has done to protect you from CA-MSRA and H1N1?</h4>
            <ul>
              <li>Equipment, showers, etc are sanitized as recommended by health professionals. 
              </li>
              <li>More cleaning spray bottles (hospital-grade sanitizer) have been installed throughout the facility.</li>
              <li> Antibiotic hand sanitizers have been installed throughout the facility.
                
              </li>
