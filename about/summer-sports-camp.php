            <h3>Activities at Summer Camp:</h3>

            <p>Campers will have the opportunity to utilize the amazing Texas Tech Recreational facilities. They will enjoy activities administered by trained Counselors. Each session may include activities such as archery, rock climbing, team sports, cooperative games, swimming lessons and other water activities. Formal swim lessons will be taught by certified swim instructors and a snack will be provided each day. Children should arrive each day ready for swim lessons and have appropriate attire to change into after swimming (i.e. towel, sneakers, athletic attire, sunscreen and bug spray). In case of inclement weather, camp will continue.</p>

            <h3>Swim Lessons:</h3>
            <p>Sessions 1, 2 and 3   include swim lessons. Swimmers must have passed all previous swimming levels in order to advance to the next level. All children may be moved up or down in levels at the discretion of our Red Cross Certified Water Safety Instructors. We are committed to giving your child the best learning experience possible, and want them to be able to both learn and succeed at our swimming lessons.</p>
            <p>Listed below are the swimming levels for each set of swim lessons. For formal class descriptions, <a href="../youth/images/Course Description Grid.pdf" class="pdf">please click here</a>.</p>
            <ul>
                <li>LEVEL 1: Intro to Water Skills</li>
                <li>LEVEL 2: Fundamental Aquatic Skills</li>
                <li>LEVEL 3: Stroke Development</li>
                <li>LEVEL 4: Stroke Improvement</li>
                <li>LEVEL 5: Stroke Refinement</li>
                <li>LEVEL 6: Swimming and Skill Proficiency</li>
            </ul>

            <p>When registering your camper, you will be asked to place them in a swim level. Be very aware of your camper's aquatic skill and ability so that we may place them in the proper swim group. Please communicate and questions, comments, or concerns with camp staff prior to the first day.</p>

            <h3>Activities at Senior Camp:</h3>

            <p>Our Senior Camp is a one-week session designed for            
            older campers. A camp day begins with open swimming at the Texas Tech Leisure Pool and continues with unique recreation opportunities most campers have likely never experienced. </p>

            <p>This camp does not play competitive sports like baseball or basketball, and it is not a goal of the camp to improve camper skill or technique for any sporting event or competition. The goal of this older camp is to allow campers expierence in recreational activities that they would likely not have elsewhere. The Student Recreation Center houses many unique opportunities across the department that are utilized for this one week session.</p>

