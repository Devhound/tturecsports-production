                <h3>Texas Tech University and Texas Tech Health Sciences Center Faculty/Staff</h3>
                <p>TTU and TTUHSC faculty and staff are eligible to purchase a Recreational Sports membership by stopping by the Main Office of the Rec Center, room 202 and presenting their vaild TTU or TTUHSC ID card. TTU and TTUHSC faculty and staff can also self sponsor membership for a single day by presenting your ID at our ID stations and paying the $10 faculty/staff one day pass fee.</p>

                <h3>Spouses</h3>
