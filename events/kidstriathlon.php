<h3> How does a Triathlon work?</h3>
<p>A triathlon has 3 events with a transition area in between to change equipment and clothes.          </p>
<p><strong>The Swim:</strong><br /> 
  Swimmers will go off 1 by 1 at 15 second intervals. They go in the water fastest swimmer to slowest swimmer. That is why we ask for a seed time.  The more accurate the seed time you give us, the less chance of passing/being passed (and less waves). They will push off the wall for the start (no diving). They will swim up one lane, flip (or duck) under the lane line and swim back the next lane. The pool is 25 yards long and is 4 feet, 6 inches deep. The younger age group will swim 4 lengths and the older group will swim 8 lengths. They will exit the pool at a ladder and there will be carpet leading to the exit for them to follow. There will be a time gap between the age groups to give the younger age groups time to complete the cycling before the older group begins. How long will depend on the number of entries. </p>
<p><strong>First transition:          </strong><br />
  Participants run (carefully because they will not have shoes on) following the carpet to the transition area where their bike is and the pile of clothes that they want to wear. Parents and other helpers are not allowed in the transition area after the triathlon begins. Children must change clothes, put on shoes etc before mounting the bike. Helmets must be BUCKLED and bikes must be WALKED out of the transition area. There will be a red tape line on the driveway indicating where they can get on their bikes.</p>
<p> <strong>Cycling: </strong><br />
Please see the map shown for bike course.  Participants will ride the emergency road entrance and turn right onto Main Street (road will be closed to traffic), turn right onto Flint and right onto 18th Street (roads are not closed to traffic) right onto Indiana and right on Main Street (roads closed to traffic). There will be police at the corners.  7-10 age group does 3 laps; 11-14 age group does 4 laps.  Counting laps is up to participants.  On the last lap, participants will turn right onto the emergency road back towards the transition.  They must dismount their bikes at the read line and walk/jog it back to its spot in the transition. Here are a few hints:
</p>
<ul>
  <li>Make sure shoe laces are tucked in</li>
  <li> Keep helmet on and buckled at all times  </li>
  <li>Follow police and corner marshal directions</li>
  <li> Ride to the right of the street, but watch out for the gravel.</li>
  <li> Pass on the left, but not in the center of the road</li>
  <li> Find a grown up if you decide to stop, or need help</li>
  <li> Walk bike back into the bike area from the red line </li>
</ul>
<p><strong>The 2nd Transition:
          </strong><br />
        After the bike has been racked and the helmet off, the triathletes will begin their run. Please see the attached map for directions. There will be cups of water available for participants as they leave the transition area to the run. </p>
<p><strong>The Run:  </strong><br />
  The running event is on the jogging track/sidewalk around the Rec Center Both courses are an out an back run.  Runners need to stay on the left side of the jogging track.  The turn around point for each will be a Y in the road.  Please see the map.  Every finisher will be awarded a medal as they cross the finish line. No one can run with the child. </p>
<p><strong>The End:</strong> <br />
  There will be refreshments for participants. We encourage you to stay and cheer on others. Drawings for prizes will be held after everyone is finished. For the safety and fairness of all participants, please do not remove bikes from the transition area until the last triathlete has begun the run.          </p>
<h3>What do I need to bring?</h3>
<ul>
  <li>  A swim suit. Goggles are not mandatory but most swimmers like them. (no flippers, kickboards or flotation devices allowed)
  </li>
  <li>A bike that has working brakes and an approved bike helmet that fits your head. Sorry, training wheels are not allowed.</li>
  <li> Running shoes          </li>
</ul>
<h3>How much can parents help?</h3>
<p> The best help that parents can give is in making sure your child is ready to compete. </p>
<ul>
  <li>Make sure their bike is in working order (especially the brakes) and that their helmet fits. We anticipate kids to be on kid’s bikes—a racing bike is not expected—so please don’t feel intimidated but make sure it is a safe bicycle!          </li>
  <li>Take them swimming to make sure they can complete the distance. No flotation devices are allowed. The pool depth is 4 feet, 6 inches. They can rest at the ends of the pool if necessary. You might swim with them in a lane and make some splashes because this will probably occur.</li>
  <li> Find a bike course around your neighborhood of a similar distance (or ride at Tech in the evenings/weekend if you live in Lubbock). Make sure they understand that they need to stay to the right side of the road (with traffic). Teach them that if they want to pass another cyclist, they should pass on the left of the other cyclist but fairly close—not in the middle of the road. Follow behind them on your bike as they get better to let them lead and gently remind them when they veer to the left or right. Get them to tuck their shoes lace bows into the laces or the outside of their shoes so the laces will not get caught in chain ring. <strong>They will make many right turns so please  practice these.  Make sure that their  inside pedal is NOT in the 6 o’clock position as they make the turn (so it  won’t catch on the ground).</strong></li>
<li> Run with them on a similar distance course in the neighborhood, at a track (or run at Tech in the evenings/weekend if you live in Lubbock).</li>
  <li> For all of these activities, please start early and work up to the distances. Then try to combine 2 of the activities at one time and, if possible, do all 3 at least once before the triathlon. Give them positive encouragement. Let them figure out what clothes they are going to wear, how they are going to change (i.e. I am going to put my glasses in my shoes so they won’t get broken; I am going to stack my shorts on my shoes with my shirt on top) and practice transitioning from one event to another.</li>
  <li>  When you come to pick up your packet (if not before), please drive (or ride) them on the cycling course and drive, walk or run the running course so they will feel comfortable knowing where they are going. We will have plenty of volunteers along the way, but they will be more confident if they know where they are going and “how much further” until the end.  </li>
</ul>
