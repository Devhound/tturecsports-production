  <li> If your child is going to compete in just his/her swim suit, you will have to have some sort of elastic belt with the number attached to it that they can slip around their waist for the run. ALL PARTICIPANTS MUST HAVE A NUMBER ON FOR THE RUN.          </li>
  <li>Put all numbers on clothing, bike the night before.Try to get your child to bed early as it will be an early awakening on race day.</li>
  <li>  Have the car packed as much as possible.</li>
</ul>
