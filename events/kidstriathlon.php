<h3>What time should we be there?</h3>
<p> We will be open at 6:30 am. Everyone will have their arms and legs numbered with a magic marker and bikes/transition area will need to be set up. Please do not come later than 7 am in order to be ready on time. Everyone (even the older age group) must be at the pool for instructions at 7:15 am. Parents and helpers are allowed in the transition area before the race begins to help organize the bike, helmet, towel and pile of clothes/shoes. Placement of bike in the transition area is on a first come, first placed basis. Once the race begins, only participants are allowed in the transition area. Competitors will not be allowed to remove their bike and/gear from the transition area until the last competitor has begun the run.</p>
<h3> What if a child needs medical attention?  </h3>
<p>We will have plenty of volunteers to contact the race directors who are CPR certified. EMS will be called.</p>
<h3> What if a child does not finish?  </h3>
<p>Please report this to a race volunteer so we will know. Please encourage the child to stay and enjoy the day and cheer on others.  </p>
<h3>Are there awards?  </h3>
<p>The emphasis of this triathlon is in the name—Fit4 Fun—so there will be no awards for the fastest competitors but each child will receive a medal around his or her neck as(s)he crosses the finish line. Please plan to stay after the event as we will have random drawings for some great prizes. We will post individual times by Monday afternoon on this website for those who wish to see how they did.  </p>
<h3>What happens if the weather is bad?</h3>
<p> The race will take place rain or shine, unless the conditions are severe enough to cancel because of safety concerns. The directors have the right to shorten distances or delete a section if safety is an issue.</p>

